package com.pucrs.ex_email.ex4;

public class Plateau {
	private int BottomX;
	private int LeftY;
	private int MaxX;
	private int MaxY;

	public int getBottomX() {
		return BottomX;
	}

	public void setBottomX(int bottomX) {
		BottomX = bottomX;
	}

	public int getLeftY() {
		return LeftY;
	}

	public void setLeftY(int leftY) {
		LeftY = leftY;
	}

	public int getMaxX() {
		return MaxX;
	}

	public void setMaxX(int maxX) {
		MaxX = maxX;
	}

	public int getMaxY() {
		return MaxY;
	}

	public void setMaxY(int maxY) {
		MaxY = maxY;
	}
}
